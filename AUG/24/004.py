# Creat a program to sum of three number from the user input

num1 = int(input("enter the value num1"))
num2 = int(input("enter the value num2"))
num3 = int(input("enter the value num3"))


def sum_of_num(num1, num2, num3):
    return num1 + num2 + num3


result = sum_of_num(num1, num2, num3)
print(result)
